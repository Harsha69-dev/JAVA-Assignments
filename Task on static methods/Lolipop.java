class Lolipop {

    void unWrap() {
        System.out.println("Unwrapping the lollipop");
    }

    void lick() {
        System.out.println("Licking the lollipop");
    }

    void suck() {
        System.out.println("Sucking the lollipop");
    }

    void bite() {
        System.out.println("Biting the lollipop");
    }

    void dustBin() {
        System.out.println("Putting the lollipop wrapper in the dustbin");
    }

    void checkFlavor() {
        System.out.println("Checking the lollipop flavor");
    }

    void shareWithFriend() {
        System.out.println("Sharing the lollipop with a friend");
    }

    void store() {
        System.out.println("Storing the lollipop for later");
    }

    void meltInMouth() {
        System.out.println("Lollipop is melting in the mouth");
    }

    void finishEating() {
        System.out.println("Finished eating the lollipop");
    }
}
